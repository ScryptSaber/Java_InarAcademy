package chapters.chapter10.checkpoint10;

public class Checkpoint10_11 {
    public static void main(String[] args) {
        System.out.println("Integer.parseInt(\"10\") = " + Integer.parseInt("10"));
        System.out.println("Integer.parseInt(\"10\", 10) = " + Integer.parseInt("10", 10));
        System.out.println("Integer.parseInt(\"10\", 16) = " + Integer.parseInt("10", 16));
        System.out.println("Integer.parseInt(\"11\") = " + Integer.parseInt("11"));
        System.out.println("Integer.parseInt(\"11\", 10) = " + Integer.parseInt("11", 10));
        System.out.println("Integer.parseInt(\"11\", 16)) = " + Integer.parseInt("11", 16));
    }
}
